/// <summary>
/// Единый источник правды для размерностей игрового мира, общих для ВСЕХ сцен
/// (тюрьма, гонка, комната памяти). Здесь живут только универсальные метрики,
/// которые обязаны совпадать между сценами, чтобы тайл и персонаж выглядели
/// одинаково везде. Раскладка конкретного уровня (размер сетки тюрьмы, длина
/// трассы гонки и т.п.) — это дизайн уровня и остаётся в его собственном
/// скрипте, а не здесь.
///
/// Менять размер мира/персонажей нужно ТОЛЬКО тут.
/// </summary>
public static class WorldMetrics
{
    // --- Сетка и тайлы -----------------------------------------------------
    /// <summary>Размер одной клетки мира в юнитах. Базовая единица всего.</summary>
    public const float CellSize = 1f;

    /// <summary>
    /// Лёгкий нахлёст соседних тайлов. Скрывает швы между point-фильтрованными
    /// текстурами, которые проступают при движущейся (sub-pixel) камере.
    /// </summary>
    public const float TileOverlap = 1.02f;

    /// <summary>Высота «бортика» стены (псевдо-3D смещение верх/бок).</summary>
    public const float WallHeight = 0.6f;

    // --- Масштабы персонажей (доля клетки по высоте кадра 64px) -------------
    /// <summary>Игрок, NPC, заключённые, бегуны гонки — обычные люди.</summary>
    public const float CharacterScale = 1.55f;

    /// <summary>Надзиратели: чуть компактнее обычных персонажей.</summary>
    public const float GuardScale = 1.45f;

    // --- Двери -------------------------------------------------------------
    /// <summary>Доля клетки, которую закрытая створка заполняет в проёме.</summary>
    public const float DoorClosedFill = 0.98f;

    /// <summary>Ширина створки в открытом состоянии (полоска в косяке).</summary>
    public const float DoorOpenSliver = 0.16f;
}
